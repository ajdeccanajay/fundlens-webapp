async function addSampleData() {
  console.log('🚀 Adding Sample Financial Data...');
  
  let created = 0;
  let updated = 0;

  for (const data of SAMPLE_DATA) {
    try {
      const result = await prisma.financialMetric.upsert({
        where: {
          ticker_normalizedMetric_fiscalPeriod_filingType: {
            ticker: data.ticker,
            normalizedMetric: data.normalizedMetric,
            fiscalPeriod: data.fiscalPeriod,
            filingType: data.filingType,
          },
        },
        update: {
          rawLabel: data.rawLabel,
          value: data.value,
          periodType: data.periodType,
          statementType: data.statementType,
          confidenceScore: data.confidenceScore,
          filingDate: new Date('2024-12-31'),
          statementDate: new Date('2024-12-31'),
          updatedAt: new Date(),
        },
        create: {
          ticker: data.ticker,
          normalizedMetric: data.normalizedMetric,
          rawLabel: data.rawLabel,
          value: data.value,
          fiscalPeriod: data.fiscalPeriod,
          periodType: data.periodType,
          filingType: data.filingType,
          statementType: data.statementType,
          confidenceScore: data.confidenceScore,
          filingDate: new Date('2024-12-31'),
          statementDate: new Date('2024-12-31'),
        },
      });

      if (result.createdAt.getTime() === result.updatedAt.getTime()) {
        created++;
      } else {
        updated++;
      }

      console.log(`✅ ${data.ticker} ${data.normalizedMetric}: ${data.value.toLocaleString()}`);
    } catch (error) {
      console.error(`❌ Error adding ${data.ticker} ${data.normalizedMetric}:`, error.message);
    }
  }

  console.log(`\n📊 Summary:`);
  console.log(`   Created: ${created}`);
  console.log(`   Updated: ${updated}`);
  console.log(`   Total: ${created + updated}`);
  console.log(`\n✅ Sample data added successfully!`);
}

async function main() {
  try {
    await addSampleData();
  } catch (error) {
    console.error('❌ Error:', error);
  } finally {
    await prisma.$disconnect();
  }
}

if (require.main === module) {
  main();
}

module.exports = { addSampleData };